daynum = int(input("What day of the week is it? Please enter the number for the corresponding day of the week:"))
if daynum == 1:
    print("Monday")
elif daynum == 2:
    print("Tuesday")
elif daynum == 3:
    print("Wednesday")
elif daynum == 4:
    print("Thursday")
elif daynum == 5:
    print("Friday")
elif daynum == 6:
    print("Saturday")
elif daynum == 7:
    print("Sunday")
else:
    print("There's only 7 days in a week!")
